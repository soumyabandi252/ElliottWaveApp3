    tabs = st.tabs(tab_names)

    for tab, name in zip(tabs, tab_names):
        with tab:
            df = sheets_dict[name]

            if df is None or df.empty:
                st.info(f"No rows available for {name}.")
                continue

            # Restrict to the specified columns ONLY on the Dashboard tab.
            # All other tabs remain unchanged (AS-IS).
            if str(name).strip().lower() == "dashboard":
                available_cols = [c for c in DASHBOARD_COLUMNS if c in df.columns]
                missing_cols = [c for c in DASHBOARD_COLUMNS if c not in df.columns]
                if missing_cols:
                    st.caption(f"\u26A0\uFE0F Columns not found in Dashboard sheet and skipped: {', '.join(missing_cols)}")
                df = df[available_cols]

            # Apply UI Filters
            view = apply_excel_style_filters(df, key_prefix=name)

            st.caption(f"Showing {len(view)} of {len(df)} rows. Click column headers to sort.")

            # Create Pandas Style object for coloring
            styled_df = style_dataframe(view)

            # Native, paginated Streamlit DataFrame
            st.dataframe(
                styled_df,
                use_container_width=True,
                height=650,
                hide_index=True
            )

            st.download_button(
                f"\U0001F4E5 Download {name} Data as CSV",
                view.to_csv(index=False),
                file_name=f"{name}.csv",
                mime="text/csv",
                key=f"dl_{name}"
            )
