def dedupe_columns(cols):
    clean = []
    for c in cols:
        c_str = str(c).strip()
        if pd.isna(c) or c_str == "" or c_str.lower() == "nan":
            clean.append("Unnamed")
        else:
            clean.append(c_str)
    counts = {}
    result = []
    for col in clean:
        if col in counts:
            counts[col] += 1
            result.append(f"{col}_{counts[col]}")
        else:
            counts[col] = 0
            result.append(col)
    return result


@st.cache_data(ttl=300)
def load_workbook():
    if not os.path.exists(OUTPUT_XLSX):
        return {}, None

    mtime = datetime.fromtimestamp(os.path.getmtime(OUTPUT_XLSX))
    sheets = pd.read_excel(OUTPUT_XLSX, sheet_name=None, engine="openpyxl")

    cleaned = {}
    for name, df in sheets.items():
        try:
            df2 = df.copy()
            df2.columns = df2.iloc[1] if df2.shape[0] > 1 else df2.columns
            df2 = df2.iloc[2:].reset_index(drop=True)
            df2.columns = dedupe_columns(df2.columns)
            df2 = df2.where(pd.notna(df2), "")
            cleaned[name] = df2
        except Exception:
            cleaned[name] = df

    return cleaned, mtime


def score_bg(val):
    try:
        v = float(str(val).replace(",", "").strip())
    except Exception:
        return None
    if v >= 88:
        return "#00C853", "white"
    if v >= 78:
        return "#64DD17", "black"
    if v >= 68:
        return "#FFD600", "black"
    if v > 0:
        return "#FF6D00", "white"
    return None


