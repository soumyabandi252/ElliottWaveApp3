st.set_page_config(page_title="Elliott Wave Screener — NASDAQ Composite", layout="wide")

OUTPUT_XLSX = "Elliott_Wave_NASDAQ_Composite_Master_Workbook.xlsx"

st.title("📈 Elliott Wave Trading Screener")
st.caption("Results auto-generated by a scheduled GitHub Action — this page only reads the latest file, it never runs the scan itself.")


# ──────────────────────────────────────────────────────────────
# COLUMN CLEANUP HELPERS
# ──────────────────────────────────────────────────────────────
def dedupe_columns(cols):
    """Replace NaN/blank column names with 'Unnamed', then de-duplicate."""
    clean = []
    for c in cols:
        if pd.isna(c) or str(c).strip() == "" or str(c).lower() == "nan":
            clean.append("Unnamed")
        else:
            clean.append(str(c))
    counts = {}
    result = []
    for col in clean:
        if col in counts:
            counts[col] += 1
            result.append(f"{col}_{counts[col]}")
        else:
            counts[col] = 0
            result.append(col)
    return result


# ──────────────────────────────────────────────────────────────
# WORKBOOK LOADER (reads the committed .xlsx, no scanning here)
# ──────────────────────────────────────────────────────────────
@st.cache_data(ttl=300)
def load_workbook():
    if not os.path.exists(OUTPUT_XLSX):
        return {}, None

    mtime = datetime.fromtimestamp(os.path.getmtime(OUTPUT_XLSX))
    sheets = pd.read_excel(OUTPUT_XLSX, sheet_name=None, engine="openpyxl")

    cleaned = {}
    for name, df in sheets.items():
        try:
            df2 = df.copy()
            # First 2 rows are title/subtitle in the workbook formatter -> row index 1 is the real header
            df2.columns = df2.iloc[1] if df2.shape[0] > 1 else df2.columns
            df2 = df2.iloc[2:].reset_index(drop=True)
            df2.columns = dedupe_columns(df2.columns)
            df2 = df2.where(pd.notna(df2), None)
            cleaned[name] = df2
        except Exception:
            cleaned[name] = df

    return cleaned, mtime


# ──────────────────────────────────────────────────────────────
# COLOR STYLING (matches the Excel workbook fills exactly)
# ──────────────────────────────────────────────────────────────
def score_color(val):
    try:
        v = float(val)
    except Exception:
        return ""
    if v >= 88:
        return "background-color: #00C853; color: white"
    if v >= 78:
        return "background-color: #64DD17; color: black"
    if v >= 68:
        return "background-color: #FFD600; color: black"
    return "background-color: #FF6D00; color: white"


def state_color(val):
    s = str(val).upper()
    if "3RD OF 3RD" in s or "EXPLOSIVE" in s:
        return "background-color: #00E5FF"
    if "WAVE 3" in s or "BREAKOUT" in s:
        return "background-color: #00C853; color: white"
    if "WAVE 2" in s or "BUY ZONE" in s:
        return "background-color: #FF9800"
    if "ACCUMULATION" in s or "CAPITULATION" in s:
        return "background-color: #B9F6CA"
    if "TRIANGLE" in s:
        return "background-color: #B0BEC5"
    if any(x in s for x in ["BEAR", "EXIT", "TRAP", "BLOWOFF"]):
        return "background-color: #EF5350; color: white"
    return "background-color: #ECEFF1"


def action_color(val):
    s = str(val).upper()
    if any(x in s for x in ["BUY", "LONG", "HOLD", "ADD", "BUILD"]):
        return "background-color: #00C853; color: white; font-weight: bold"
    if any(x in s for x in ["EXIT", "AVOID", "SELL", "TRIM", "SHORT", "REDUCE"]):
        return "background-color: #EF5350; color: white; font-weight: bold"
    return "background-color: #FFD600; color: black; font-weight: bold"


SCORE_COLS = ["EWScore", "LTScore", "MTScore", "STScore", "MNScore",
              "CombinedScore", "TripleBullScore", "TomorrowBuyRank"]
STATE_COLS = ["ElliottState", "LTState", "MTState", "STState", "MNState"]
ACTION_COLS = ["RecommendedAction", "LTAction", "MTAction", "STAction", "MasterAction"]


def style_dataframe(df):
    styler = df.style
    for col in SCORE_COLS:
        if col in df.columns:
            styler = styler.applymap(score_color, subset=[col])
    for col in STATE_COLS:
        if col in df.columns:
            styler = styler.applymap(state_color, subset=[col])
    for col in ACTION_COLS:
        if col in df.columns:
            styler = styler.applymap(action_color, subset=[col])
    return styler


